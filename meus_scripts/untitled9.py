# --- 3. Calcular a margem de confiança (95%) ---

# Calcular os resíduos do conjunto de teste
residuals = y_test - y_pred_svr

# Calcular o desvio padrão dos resíduos
std_residuals = residuals.std()

# Para 95% de confiança, o valor Z-score é aproximadamente 1.96
z_score = 1.96 # Usando z-score para uma aproximação de intervalo de confiança

# Calcular a margem de erro
margin_of_error = z_score * std_residuals

# Calcular os limites inferior e superior do intervalo de confiança para as previsões futuras
lower_bound_future = y_pred_future - margin_of_error
upper_bound_future = y_pred_future + margin_of_error

# --- Inverse transform the predictions and confidence bounds to original scale ---
# Find the index of the target variable in columns_to_normalize
target_col_index = columns_to_normalize.index('Internações por doenças respiratórias')

# Prepare an array for inverse transformation of predictions and bounds
y_pred_future_original_scale = scaler.inverse_transform(np.c_[np.zeros((len(y_pred_future), target_col_index)), y_pred_future.reshape(-1, 1), np.zeros((len(y_pred_future), len(columns_to_normalize) - target_col_index - 1))])[:, target_col_index]
lower_bound_future_original_scale = scaler.inverse_transform(np.c_[np.zeros((len(lower_bound_future), target_col_index)), lower_bound_future.reshape(-1, 1), np.zeros((len(lower_bound_future), len(columns_to_normalize) - target_col_index - 1))])[:, target_col_index]
upper_bound_future_original_scale = scaler.inverse_transform(np.c_[np.zeros((len(upper_bound_future), target_col_index)), upper_bound_future.reshape(-1, 1), np.zeros((len(upper_bound_future), len(columns_to_normalize) - target_col_index - 1))])[:, target_col_index]

# Inverse transform std_residuals and margin_of_error
temp_std_residuals_array = np.zeros((1, len(columns_to_normalize)))
