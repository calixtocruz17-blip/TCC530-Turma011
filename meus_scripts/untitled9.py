# Criar um DataFrame para os anos futuros
future_years_list = [2026, 2027]
X_future = pd.DataFrame([mean_feature_values_normalized] * len(future_years_list), columns=features_for_mean_calc)
X_future['Pandemia'] = 0 # Definir 'Pandemia' como 0 para os anos futuros
X_future['Ano'] = future_years_list # Adicionar 'Ano' para identificação

