"""### 6. Previsão para Anos Futuros (2026 e 2027) e Margem de Confiança

Para realizar as previsões para os anos de 2026 e 2027, utilizaremos o modelo Gradient Boosting Machine (GBM), que se mostrou o mais eficaz. Como não temos dados futuros para todas as variáveis preditoras (poluição e outros tipos de internações), faremos as seguintes suposições:

*   **Poluição Atmosférica (Poluicao_Anual_Total):** Usaremos a média histórica dessa variável do conjunto de dados consolidado filtrado, já que não temos previsões futuras para a poluição.
*   **Internações por Doenças do Aparelho Respiratório (2026) e Internações por Doenças do Trato Respiratório Superior (2026):** Utilizaremos os valores disponíveis no `df_internacoes` para 2026 (72 e 3, respectivamente).
*   **Internações por Doenças do Aparelho Respiratório (2027) e Internações por Doenças do Trato Respiratório Superior (2027):** Para 2027, usaremos a média histórica dessas variáveis do conjunto de dados consolidado filtrado.

Após gerar as previsões, calcularemos uma margem de confiança aproximada de 95% com base nos resíduos do modelo no conjunto de teste.
"""

# Calcular médias para imputação (usando df_consolidado_filtered, que contém os anos de treinamento)
