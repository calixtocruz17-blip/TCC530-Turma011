mean_trato_superior = df_consolidado_filtered['Internações por doenças do trato respiratório superior'].mean()

# Preparar os dados para as previsões de 2026 e 2027
data_for_prediction = pd.DataFrame({
    'Ano': [2026, 2027],
    'Poluicao_Anual_Total': [mean_poluicao, mean_poluicao],
    'Internações por doenças do aparelho respiratório': [72, mean_aparelho_respiratorio],
    'Internações por doenças do trato respiratório superior': [3, mean_trato_superior],
    'Internações por doenças respiratórias': [0, 0] # Placeholder para a coluna alvo para escalar corretamente
})

# Escalar os dados de previsão usando o mesmo scaler ajustado nos dados de treinamento
# numerical_cols_annual inclui a variável alvo, então escalamos o conjunto completo de colunas.
X_predict_scaled_full = scaler_annual.transform(data_for_prediction[numerical_cols_annual])

# Extrair apenas as colunas preditoras escaladas (as três primeiras)
X_predict_scaled = X_predict_scaled_full[:, :3]

# Gerar previsões usando o modelo GBM
y_pred_future_scaled = gbm_model.predict(X_predict_scaled)

# Inverter a transformação das previsões para a escala original
# Para fazer isso, precisamos criar um array 'dummy' que corresponda ao espaço de features original para o scaler
dummy_inverse_transform = np.zeros_like(X_predict_scaled_full)
dummy_inverse_transform[:, -1] = y_pred_future_scaled # Colocar as previsões na posição da coluna alvo

predictions_future_original_scale = scaler_annual.inverse_transform(dummy_inverse_transform)[:, -1]

# Exibir as previsões
print("Previsões para 2026 e 2027 (Internações por doenças respiratórias):")
print(f"2026: {predictions_future_original_scale[0]:.0f}")
print(f"2027: {predictions_future_original_scale[1]:.0f}")

# Calcular a margem de confiança
# Usando os resíduos do conjunto de teste para o modelo GBM
residuals_gbm = y_test - y_pred_gbm
std_residuals_scaled = np.std(residuals_gbm)

# Corrigir a obtenção do fator de escala para a variável alvo
# O fator de escala é a diferença entre o valor máximo e mínimo da variável alvo original
min_target_original = df_consolidado_filtered['Internações por doenças respiratórias'].min()
max_target_original = df_consolidado_filtered['Internações por doenças respiratórias'].max()
range_target_original = max_target_original - min_target_original

# Aproximar o intervalo de confiança de 95% usando o z-score (1.96 para distribuição normal)
z_score_95 = 1.96
margin_of_error_scaled = z_score_95 * std_residuals_scaled

# Converter a margem de erro para a escala original usando o range correto
margin_of_error_original_scale = margin_of_error_scaled * range_target_original

print("\nMargem de Confiança (95% CI) para as Previsões:")
print("------------------------------------------------")

lower_bound_2026 = predictions_future_original_scale[0] - margin_of_error_original_scale
upper_bound_2026 = predictions_future_original_scale[0] + margin_of_error_original_scale
print(f"2026: {predictions_future_original_scale[0]:.0f} (CI 95%: [{lower_bound_2026:.0f}, {upper_bound_2026:.0f}])")

lower_bound_2027 = predictions_future_original_scale[1] - margin_of_error_original_scale
upper_bound_2027 = predictions_future_original_scale[1] + margin_of_error_original_scale
print(f"2027: {predictions_future_original_scale[1]:.0f} (CI 95%: [{lower_bound_2027:.0f}, {upper_bound_2027:.0f}])")

print("\n**Nota sobre a Margem de Confiança:**")
print("Esta margem de confiança é uma aproximação baseada na distribuição dos resíduos do conjunto de teste do modelo GBM. Ela assume uma distribuição normal dos erros e homoscedasticidade. Para uma análise de incerteza mais robusta, métodos como bootstrap ou pacotes especializados para intervalos de predição em modelos de ensemble seriam mais apropriados.")

