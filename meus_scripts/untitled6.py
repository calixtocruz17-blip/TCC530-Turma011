"""### Divisão da Base de Dados para Treinamento e Teste

Para avaliar o desempenho do nosso modelo de forma robusta, dividiremos os dados normalizados em conjuntos de treinamento e teste. Isso garante que o modelo seja treinado com uma parte dos dados e avaliado em dados que ele não viu durante o treinamento, fornecendo uma estimativa mais realista de sua capacidade de generalização.
"""

from sklearn.model_selection import train_test_split

# Dividir os dados em conjuntos de treinamento e teste
# Usaremos 80% para treinamento e 20% para teste, com um random_state para reprodutibilidade
