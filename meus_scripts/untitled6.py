"""### Construção e Avaliação de Modelos Preditivos

Vamos agora construir e avaliar diferentes modelos de regressão para prever as 'Internações por doenças respiratórias'. Começaremos com o Random Forest Regressor, que é um algoritmo de ensemble robusto e amplamente utilizado.

#### 1. Random Forest Regressor

O Random Forest é um algoritmo versátil que pode ser usado para tarefas de classificação e regressão. Ele constrói múltiplas árvores de decisão e as une para obter uma previsão mais precisa e estável. Vamos treiná-lo com nossos dados e avaliar seu desempenho.
"""

from sklearn.ensemble import RandomForestRegressor
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

# Inicializar e treinar o modelo Random Forest Regressor
rf_model = RandomForestRegressor(n_estimators=100, random_state=42) # n_estimators é o número de árvores na floresta
rf_model.fit(X_train, y_train)

# Fazer previsões no conjunto de teste
y_pred_rf = rf_model.predict(X_test)

# Avaliar o desempenho do modelo
mae_rf = mean_absolute_error(y_test, y_pred_rf)
mse_rf = mean_squared_error(y_test, y_pred_rf)
rmse_rf = mse_rf**0.5 # Root Mean Squared Error
r2_rf = r2_score(y_test, y_pred_rf)

print("Desempenho do Random Forest Regressor:")
print(f"MAE: {mae_rf:.4f}")
print(f"MSE: {mse_rf:.4f}")
print(f"RMSE: {rmse_rf:.4f}")
print(f"R-squared: {r2_rf:.4f}")

"""#### 2. Gradient Boosting Machine (GBM)

O Gradient Boosting é outro poderoso algoritmo de ensemble que constrói modelos sequencialmente, onde cada novo modelo tenta corrigir os erros dos modelos anteriores. Ele geralmente oferece alta precisão.
"""

from sklearn.ensemble import GradientBoostingRegressor

# Inicializar e treinar o modelo Gradient Boosting Regressor
gbm_model = GradientBoostingRegressor(n_estimators=100, learning_rate=0.1, max_depth=3, random_state=42)
