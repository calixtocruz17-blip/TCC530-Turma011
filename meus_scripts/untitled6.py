"""### Definição das Variáveis (X e y) e Divisão da Base de Dados

Vamos definir nossa variável alvo (`y`) como `'Internações por doenças respiratórias'` e as variáveis preditoras (`X`) como todas as outras colunas relevantes do DataFrame normalizado, excluindo o `'Ano'` e a própria variável alvo.
"""

# Variável alvo (y)
